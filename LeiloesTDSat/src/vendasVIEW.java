    private void listarProdutosVendidos(){
        // instanciando a classe produtosdao
        ProdutosDAO produtosDAO = new ProdutosDAO();
        
        // criando a tabela
        try{
        DefaultTableModel model = (DefaultTableModel) tblvendas.getModel();
        // zerando as linhas
        model.setNumRows(0);
        
            // criando um array de produtosdto
            ArrayList<ProdutosDTO> listagem  = produtosDAO.listarProdutosVendidos();
            // criando um for para percorrer toda a tabela
            for(int i = 0; i< listagem.size(); i++){
                model.addRow(new Object[]{
                listagem.get(i).getId(),
                listagem.get(i).getNome(),
                listagem.get(i).getValor(),
                listagem.get(i).getStatus()
            });
                
            }
        }catch(Exception e ){
            JOptionPane.showMessageDialog(null, "NÃ£o foi possivel listar os produtos");
        }
        
    
    }

